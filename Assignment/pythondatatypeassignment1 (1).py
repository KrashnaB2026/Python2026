'''18. Write a python function that receives a list of integers and prints out a histogram of bin size 10'''

	# Input:
	# [13,42,15,37,22,39,41,50]
	# Output:
	# {11-20:2,21-30:1,31-40:2,41-50:3}

def print_histogram(nums):
    if not nums:
        print("{}")
        return

    min_val = min(nums)
    max_val = max(nums)

    bins_start = ((min_val - 1) // 10) * 10 + 1
    bins_end = ((max_val + 9) // 10) * 10

    hist = {}

    for start in range(bins_start, bins_end + 1, 10):
        end = start + 9
        count = sum(1 for x in nums if start <= x <= end)

        if count > 0:
            hist[f"{start}-{end}"] = count

    print(hist)


nums = [13,42,15,37,22,39,41,50]
print_histogram(nums)







