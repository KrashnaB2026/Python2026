	# Input:
	# ['1ac21', '23fg', '456', '098d','1','kls']

	# Output:
	# ['456', '23fg', '1ac21', '1', 'kls', '098d']

lst = ['1ac21', '23fg', '456', '098d','1','kls']

def product_value(s):
    product = 1
    found = False

    for ch in s:
        if ch.isdigit():
            product *= int(ch)
            found = True

    if not found:
        return 1
    return product

result = sorted(lst, key=product_value, reverse=True)

print(result)

