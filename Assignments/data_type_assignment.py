"""7. Sort a list of alphanumeric strings based on product value of numeric character in it. If in any string there is no numeric character take it's product value as 1.

	Input:

	['1ac21', '23fg', '456', '098d','1','kls']
	Output:

	['456', '23fg', '1ac21', '1', 'kls', '098d']

"""

from functools import reduce

# Given list
data = ['1ac21', '23fg', '456', '098d', '1', 'kls']

# Function to calculate product of digits
def product_of_digits(s):
    digits = [int(ch) for ch in s if ch.isdigit()]
    if not digits:
        return 1
    return reduce(lambda x, y: x * y, digits)

# Sort based on product of digits
result = sorted(data, key=lambda x: product_of_digits(x), reverse=True)

# Output
print(result)

