"""18. Write a python function that receives a list of integers and prints out a histogram of bin size 10

	Input:
	[13,42,15,37,22,39,41,50]
	Output:
	{11-20:2,21-30:1,31-40:2,41-50:3}

"""

def histogram_bin10(numbers):
    bins = {}

    for num in numbers:
        # Find bin range
        lower = (num // 10) * 10 + 1
        upper = lower + 9

        # Fix for multiples of 10 (like 10, 20, 30...)
        if num % 10 == 0:
            lower -= 10
            upper -= 10

        key = f"{lower}-{upper}"

        # Count frequency
        bins[key] = bins.get(key, 0) + 1

    # Sort bins properly
    sorted_bins = dict(sorted(bins.items(), key=lambda x: int(x[0].split('-')[0])))

    print(sorted_bins)


# Input
data = [13, 42, 15, 37, 22, 39, 41, 50]

# Function call
histogram_bin10(data)