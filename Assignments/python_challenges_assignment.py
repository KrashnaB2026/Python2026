# Student Marks Analyzer

students = {}

print("Enter student data (type 'done' to finish)\n")

# Input loop
while True:
    name = input("Enter student name: ")

    if name.lower() == "done":
        break

    if name in students:
        print(" Duplicate name not allowed! Try again.\n")
        continue

    try:
        marks = int(input("Enter marks: "))

        if marks < 0:
            print("Marks cannot be negative!\n")
            continue

        students[name] = marks

    except ValueError:
        print(" Please enter a valid integer for marks!\n")

# If no data entered
if not students:
    print("\nNo student data entered.")
else:
    total_students = len(students)
    total_marks = sum(students.values())
    average = total_marks / total_students

    highest_mark = max(students.values())
    lowest_mark = min(students.values())

    highest_students = [name for name, mark in students.items() if mark == highest_mark]
    lowest_students = [name for name, mark in students.items() if mark == lowest_mark]

    # Report Output
    print("\n" + "=" * 40)
    print("        STUDENT MARKS REPORT")
    print("=" * 40)

    print(f"Total Students : {total_students}")
    print(f"Average Marks  : {average:.2f}")

    print(f"\nHighest Marks  : {highest_mark}")
    print("Top Student(s) :", ", ".join(highest_students))

    print(f"\nLowest Marks   : {lowest_mark}")
    print("Lowest Student(s):", ", ".join(lowest_students))

    print("=" * 40)

